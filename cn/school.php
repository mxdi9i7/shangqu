宾夕法尼亚大学共有4个本科生院和12个研究生院。它位于宾夕法尼亚州的费城。宾大作为8个享有威名的常春藤学院之一，成立于1740年，他的创始人是本杰明.弗兰克林和费城的贵格会教徒，是全美第一个非宗教大学。

