                    <p>
                    宾夕法尼亚大学沃顿商学院位于费城，是世界最著名的商学院之一。沃顿商学院创立于1881年，是美国第一所大学商学院。学校的使命就是通过总结培养领导人才来促进世界的发展。
沃顿在商业实践的各个领域有着深远的影响，包括全球策略，金融，风险和保险，卫生保健，法律与道德，不动产和公共政策等。它的商业教育模式是在教学，研究，出版和服务中处处强调领导能力，企业家精神，创新能力。

