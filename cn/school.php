                    世界第一商学院的故事始自美国费城企业家约瑟夫沃顿（Joseph Wharton），他出生于一个19世纪费城富有的商业家庭，通过经营伯利恒钢铁公司和美国镍公司积累了一笔可观的财富。在1881年他55岁时，由于没有儿子沿承他的家族姓氏和经商智慧，便用其资产的一部分捐给宾州大学建立了沃顿商学院。他的初衷是培养那些继承了前辈财富的年轻人成为国家新的商业领袖。自沃顿建校之后，两任主任和11任院长打造了沃顿严谨、务实、创新、进取的校风。    
